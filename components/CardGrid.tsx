import Image from 'next/image';
import Link from 'next/link';
import type { SectionItem } from '@/types';

interface CardGridProps {
  items: SectionItem[];
  columns?: 2 | 3 | 4;
  variant?: 'card' | 'feature' | 'service';
}

export default function CardGrid({
  items,
  columns = 3,
  variant = 'card',
}: CardGridProps) {
  const gridClasses = {
    2: 'grid-cols-1 md:grid-cols-2',
    3: 'grid-cols-1 md:grid-cols-2 lg:grid-cols-3',
    4: 'grid-cols-1 sm:grid-cols-2 lg:grid-cols-4',
  };

  return (
    <div className={`grid ${gridClasses[columns]} gap-6 md:gap-8`}>
      {items.map((item) => (
        <Card key={item.id} item={item} variant={variant} />
      ))}
    </div>
  );
}

interface CardProps {
  item: SectionItem;
  variant: 'card' | 'feature' | 'service';
}

function Card({ item, variant }: CardProps) {
  if (variant === 'feature') {
    return <FeatureCard item={item} />;
  }

  if (variant === 'service') {
    return <ServiceCard item={item} />;
  }

  return <DefaultCard item={item} />;
}

function DefaultCard({ item }: { item: SectionItem }) {
  return (
    <div className="card-hover">
      {item.imageUrl && (
        <div className="relative aspect-video rounded-lg overflow-hidden mb-4">
          <Image
            src={item.imageUrl}
            alt={item.title}
            fill
            className="object-cover"
          />
        </div>
      )}
      {item.icon && (
        <div className="w-12 h-12 bg-cocoa-100 rounded-lg flex items-center justify-center mb-4">
          <IconComponent name={item.icon} className="w-6 h-6 text-cocoa-600" />
        </div>
      )}
      <h3 className="text-xl font-semibold text-cocoa-900 mb-3">{item.title}</h3>
      <p className="text-cocoa-600 mb-4">{item.description}</p>
      {item.linkText && item.linkHref && (
        <Link
          href={item.linkHref}
          className="inline-flex items-center text-navy-600 font-medium hover:text-navy-800"
        >
          {item.linkText}
          <svg
            className="w-4 h-4 ml-2"
            fill="none"
            stroke="currentColor"
            viewBox="0 0 24 24"
          >
            <path
              strokeLinecap="round"
              strokeLinejoin="round"
              strokeWidth={2}
              d="M9 5l7 7-7 7"
            />
          </svg>
        </Link>
      )}
    </div>
  );
}

function FeatureCard({ item }: { item: SectionItem }) {
  return (
    <div className="text-center p-6">
      {item.icon && (
        <div className="w-16 h-16 bg-cocoa-100 rounded-full flex items-center justify-center mx-auto mb-6">
          <IconComponent name={item.icon} className="w-8 h-8 text-cocoa-700" />
        </div>
      )}
      <h3 className="text-xl font-semibold text-cocoa-900 mb-3">{item.title}</h3>
      <p className="text-cocoa-600">{item.description}</p>
    </div>
  );
}

function ServiceCard({ item }: { item: SectionItem }) {
  return (
    <div className="group bg-white rounded-xl shadow-card overflow-hidden hover:shadow-lg transition-all duration-300">
      {item.imageUrl && (
        <div className="relative aspect-[3/2] overflow-hidden">
          <Image
            src={item.imageUrl}
            alt={item.title}
            fill
            className="object-cover group-hover:scale-105 transition-transform duration-500"
          />
        </div>
      )}
      <div className="p-6">
        {item.icon && (
          <div className="w-12 h-12 bg-navy-100 rounded-lg flex items-center justify-center mb-4 -mt-12 relative z-10 shadow-md">
            <IconComponent name={item.icon} className="w-6 h-6 text-navy-600" />
          </div>
        )}
        <h3 className="text-xl font-semibold text-cocoa-900 mb-3">
          {item.title}
        </h3>
        <p className="text-cocoa-600 mb-4">{item.description}</p>
        {item.linkText && item.linkHref && (
          <Link
            href={item.linkHref}
            className="inline-flex items-center text-navy-600 font-medium hover:text-navy-800"
          >
            {item.linkText}
            <svg
              className="w-4 h-4 ml-2 group-hover:translate-x-1 transition-transform"
              fill="none"
              stroke="currentColor"
              viewBox="0 0 24 24"
            >
              <path
                strokeLinecap="round"
                strokeLinejoin="round"
                strokeWidth={2}
                d="M9 5l7 7-7 7"
              />
            </svg>
          </Link>
        )}
      </div>
    </div>
  );
}

